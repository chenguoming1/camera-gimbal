    SSD1306_WriteCmd(hi2c, 0x10);
    SSD1306_WriteCmd(hi2c, 0x40);
    SSD1306_WriteCmd(hi2c, 0x81);
    SSD1306_WriteCmd(hi2c, 0xFF);
    SSD1306_WriteCmd(hi2c, 0xA1);
    SSD1306_WriteCmd(hi2c, 0xA6);
    SSD1306_WriteCmd(hi2c, 0xA8);
    SSD1306_WriteCmd(hi2c, 0x3F);
